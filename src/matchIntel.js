export function getH2H(teamA, teamB) {
  const key1 = `${teamA}-${teamB}`;
  const key2 = `${teamB}-${teamA}`;
  if (H2H[key1]) return { ...H2H[key1], home: teamA, away: teamB, flipped: false };
  if (H2H[key2]) {
    const d = H2H[key2];
    return { ...d, wA: d.wB, wB: d.wA, home: teamA, away: teamB,
             highA: d.highB, highB: d.highA, flipped: true };
  }
  return null;
}

